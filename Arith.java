public class Arith {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int sum = a + b;
        System.out.println("Sum: " + sum);
        int sub = a - b;
        System.out.println("Subtraction: " + sub);
        int mul = a * b;
        System.out.println("Multiplication: " + mul);
        int div = a / b;
        System.out.println("Division: " + div);
        int rem = a % b;
        System.out.println("Remainder: " + rem);

    }

}
